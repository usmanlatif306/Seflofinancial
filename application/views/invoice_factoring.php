                            </div>
                            <div class="at-description">
                                <p>
                                	Factoring is based on the money owed to your business in the form of invoices — but what happens if a customer doesn’t pay? With a recourse facility, you would have to absorb the cost of the unpaid invoice. On the other hand, with a non-recourse facility the lender would absorb the cost, leaving your business cashflow unscathed. For this reason, lenders often call non-recourse ‘bad debt protection’, because your business is protected from the issue of non-payment. However, as you might expect, this will make the factoring facility more expensive overall because the lender is accepting a higher level of risk.<br>
                                	Choosing between recourse and non-recourse will depend on the relationships you have with customers, and how likely you think a non-payment is. Of course, factoring includes credit control — so you’ll have experienced credit controllers working on your behalf to minimise this possibility — but it’s worth considering whether the risk of a recourse facility is worth the lower cost.<br>
